using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TestDrivenDevelopment
{
    class Program
    {
        static void Main(string[] args)
        {
            // Test Driven Development steps
            // 1. Get the requirements from the user - what App to build
            // 2. Give the requirements to architect/software designer for overview
            // 3. Receive specification from analysts to developer
            // 4. For each item on the requirement specification determine the proposed function
            // 5. For each function we can calculate the expected input and output
            // 6. From these inputs and outputs the developer can write some tests which expect this output from given input
            // 7. We add these tests to our application and run it
            // 8. The test will fail
            // 9. We write the minimum code to pass the test
            // 10. Once the test go from red to green i.e. fail to pass we are done 
            // 11. Final step is to refactor our code to ensure it is efficient as possible
            // 12. Repeat step 6 to 11 until all items in the requirement specification is done
        }
    }
}
